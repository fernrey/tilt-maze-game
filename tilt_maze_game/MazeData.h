int goalX = goalPositions[currentLevel][0] * cellSize; 
int goalY = goalPositions[currentLevel][1] * cellSize;

void shuffleLevels() {
  // Initialize with sequential order
  for (int i = 0; i < totalLevels; i++) {
    levelOrder[i] = i;
  }
  
  // Fisher-Yates shuffle
  for (int i = totalLevels - 1; i > 0; i--) {
    int j = random(i + 1);
    // Swap
    int temp = levelOrder[i];
    levelOrder[i] = levelOrder[j];
    levelOrder[j] = temp;
  }
  levelsShuffled = true;
}

// Function to load a level from PROGMEM into RAM
void loadLevel(int levelNum) {
  int actualLevel;
    //condition to check if levelsShuffled is true. Calls in the main file.
  if(levelsShuffled) {
    actualLevel = levelOrder[levelNum];
  } else {
    actualLevel = levelNum;
  }
  // Copy from PROGMEM to RAM
  for (int y = 0; y < mazeHeight; y++) {
    for (int x = 0; x < mazeWidth; x++) {
      maze[y][x] = pgm_read_byte(&mazes[actualLevel][y][x]);
    }
  }
  
  currentLevel = levelNum;

  goalX = goalPositions[currentLevel][0] * cellSize; 
  goalY = goalPositions[currentLevel][1] * cellSize;
}



